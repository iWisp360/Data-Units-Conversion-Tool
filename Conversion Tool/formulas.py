def b2kb():
    b = input("Express a number in Bytes: ")
    if not b.isdigit():
        print ("Only integers are allowed")
        exit(1)
    kb = (int(b) / 1024)

    print("\n" + str(b) + " bytes are " + str(kb) + " Kb :)")

