def b2kb():
    b = int(input("Expresa un número en Bytes: "))
    if (type(b) != int):
        print ("Solo admito números enteros de bytes")
        exit(1)
    kb = (b / 1024)

    print("\n" + str(b) + " bytes serían " + str(kb) + " Kb :)")

